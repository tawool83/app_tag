def main():
    root = pathlib.Path(__file__).resolve().parent.parent / "lib" / "l10n"
    for locale, keys in TRANSLATIONS.items():
        arb_path = root / f"app_{locale}.arb"
        if not arb_path.exists():
            print(f"skip (missing): {arb_path}")
            continue
        text = arb_path.read_text(encoding="utf-8")
        if "labelLogoTabShow" in text:
            print(f"skip (already has keys): {arb_path.name}")
            continue
        data = json.loads(text)
        for k, v in keys.items():
            data[k] = v
        arb_path.write_text(
            json.dumps(data, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
        print(f"updated: {arb_path.name} (+{len(keys)} keys)")


if __name__ == "__main__":
    main()
